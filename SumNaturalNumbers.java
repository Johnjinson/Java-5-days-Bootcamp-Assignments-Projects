class SumNaturalNumbers {
    public static void main(String[] args) {
        
        int n, sum;
        n = 50;
        
        sum = (n * (n + 1)) / 2;    // Formula for 1 to n natural numbers
        
        System.out.println("\nSum of 1 to 50 natural number is : " + sum);
        
    }
}
